using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace AFTC___Voice_Commander
{
    public class FormCommandEditorCommandManager
    {
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        private T t;
        public FormCommandEditor fce;
        public CommandVO cvo;
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -


        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        public FormCommandEditorCommandManager(FormCommandEditor formCommandEditor)
        {
            // Class specific debug
            t = new T(true);
            t.log("FormCommandEditorCommandManager()");
            
            fce = formCommandEditor;

            if (fce.cvoToEdit == null)
            {
                cvo = new CommandVO();
            }
            else
            {
                cvo = fce.cvoToEdit;
            }
            
        }
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -




        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        public CommandItemVO getCommandItem(int selectedIndex)
        {
            return cvo.commandItems[selectedIndex];
        }
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -




        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        public void addCommandItem(CommandItemVO civo)
        {
            t.log("FormCommandEditorCommandManager.addCommand(CommandItemVO)");

            cvo.addCommandItem(civo);

            fce.fceListController.updateCommandListView();
        }
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -



        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        public void removeCommandItem(CommandItemVO civo)
        {
            t.log("FormCommandEditorCommandManager.removeCommand(CommandItemVO)");

            cvo.commandItems.Remove(civo);

            fce.fceListController.updateCommandListView();
        }
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -


        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -
        public void removeCommandAtIndex(int index)
        {
            t.log("FormCommandEditorCommandManager.removeCommandAtIndex(index)");

            cvo.commandItems.RemoveAt(index);

            fce.fceListController.updateCommandListView();
        }
        // -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -  -


        

    }
}
